// JavaScript (JS) is a lightweight, interpreted, or just-in-time compiled programming language with first-class functions. 
// While it is most well-known as the scripting language for Web pages, 
// many non-browser environments also use it, such as Node. js, Apache CouchDB and Adobe Acrobat.

/////// What are the features of JavaScript ?   //////
// JavaScript is a lightweight, object-oriented, cross-platform scripting language, often used within web pages.
// > Lightweight (Easy to implement), interpreted programming language (JavaScript is interpreted at runtime by the client browser.) 
// > Cross-platform compatible (JavaScript is a cross-platform, object-oriented scripting language used to make webpages "interactive".)
// > Open source
// > Object-oriented 
// > Integration with other back-end and front-end technologies
// > Used especially for the development of Network-based web applications




